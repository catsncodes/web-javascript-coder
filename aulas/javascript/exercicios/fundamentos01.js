function operacoes(valor1, valor2) {
  let soma = valor1 + valor2;
  let subt = valor1 - valor2;
  let mult = valor1 * valor2;
  let divi = valor1 / valor2;
  console.log(`Soma = ${soma}`)
  console.log(`Subtração = ${subt}`)
  console.log(`Divisão = ${mult}`)
  console.log(`Multiplicação = ${divi}`)
}

operacoes(2, 3);

